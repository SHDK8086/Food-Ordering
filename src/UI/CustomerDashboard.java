    private void tblmodelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblmodelMouseClicked
        // TODO add your handling code here:
        
        DefaultTableModel tableModel = (DefaultTableModel)tblmodel.getModel ();
        
        String tblRestaurant = tableModel.getValueAt (tblmodel.getSelectedRow(), 0).toString ();
        String tblLocation = tableModel.getValueAt (tblmodel.getSelectedRow(), 1).toString ();
        String tblFooditem = tableModel.getValueAt (tblmodel.getSelectedRow(), 3).toString ();
        String tblPrice = tableModel.getValueAt (tblmodel.getSelectedRow(), 6).toString ();
        
         txtRestaurantName.setText (tblRestaurant);
         txtLocation.setText (tblLocation);
         txtFoodItem.setText (tblFooditem);
         txtPrice.setText (tblPrice);
        
 
    }//GEN-LAST:event_tblmodelMouseClicked

