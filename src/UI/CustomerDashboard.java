 private void loadFoodItems() {
    DefaultTableModel model = (DefaultTableModel) tblmodel.getModel();
    model.setRowCount(0);
    model.addColumn("Restaurant");
     model.addColumn("Location");
    model.addColumn("Category");
    model.addColumn("FoodItem");
    model.addColumn("Description");
    model.addColumn("Ratings");
    model.addColumn("Price");

    try (BufferedReader br = new BufferedReader(new FileReader("D:\\NetBeans-21\\Yummy_Eates\\menuItems.txt"))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] data = line.split(",");
            model.addRow(data);
        }
    } catch (IOException e) {
            e.printStackTrace();
        }
    }

public void billTotal() {
    
    DefaultTableModel model = new DefaultTableModel();
    model = (DefaultTableModel)tblModel2.getModel();
    
    model. addRow(new Object[]{
        
    txtLocation.getText(),
    txtRestaurantName.getText(),
    txtFoodItem.getText (),
    txtPrice.getText(),
    txtQuantity.getText(),
    txtTotal.getText()

    });
    
    int SubTotal = 0;
    for( int i = 0;  i < tblModel2.getRowCount(); i++){
        SubTotal = SubTotal+Integer.parseInt(tblModel2.getValueAt(i,  5).toString());
        
        txtSubTotal.setText(Integer.toString(SubTotal));
    }
}

